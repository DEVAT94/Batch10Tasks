class Ingredients {
  String nudelsalatRecipe() {
    return '''
400 g Nudeln, 
300 ml passierte Tomaten,
50 ml Olivenöl,
1 TL Salz,
1 TL Pfeffer,
1 TL italienische Kräuter 
''';
  }
}

class Directions {
  String nudelsalatDirection() {
    return '''Die Nudeln nach Packungsanweisung kochen 
    und unter kaltem Wasser abtropfen lassen. 
    Gurken in Scheiben schneiden. Fleischwurst würfeln.
In einer Schüssel Joghurt, Mayonnaise, Gurkenwasser, 
Hühnerbrühe, Paprikapulver und Knoblauchpulver 
vermischen und zu den Nudeln geben. Mais, Erbsen, 
Gurken und die Fleischwurst zu den Nudeln fügen 
und alle Zutaten miteinander vermengen.
Dieser Nudelsalat muss nicht durchziehen.''';
  }
}
